#include <iostream>

int main()
{
	// --------------------------------------------
	//  3. continue, break
	// --------------------------------------------
	for (int i = 0; i < 5; i++)
	{
		if (i == 3)
		{
			break;
		}

		printf("i : %d\n", i);
	}

	// continue
	//   : 이번만 스킵 (본문이 끝났다 취급)

	// break
	//   : 반복문 강제 탈출

/*\
	+-------------------+
	|    i = 0 (초기식)  |
	+-------------------+
			|
			v
	+-------------------------------------+
	|    i < 3 (조건식)                    |
	+-------------------------------------+
			| yes                 | no
			v                     v
	+--------------+              +--------------+
	|    본문실행   |  -------->   |  for문 종료  |
	+--------------+   break문    +--------------+
			|
			v
	+---------------------------------------+
	|    i++, (증감식) (조건식부터 다시 실행) |
	+---------------------------------------+

	*/

}